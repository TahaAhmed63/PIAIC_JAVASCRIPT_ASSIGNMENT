var num1=parseInt(prompt("type a number?"));
var num2=parseInt(prompt("type a second number?"))
var operation=prompt("type a operation?");
if(operation=="+"){
alert(num1+num2)
}else if(operation=="-"){
  alert(num1-num2)
}else if(operation=="*"){
alert(num1*num2)
}else if(operation=="%"){
alert(num1%num2)
}

